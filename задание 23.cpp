#include <iostream>
#include <cmath>
using namespace std;

/**
* @brief Основная функция программы
* @return 0 при успешном выполнении
*/
int main() {
    float a, b, c;
    
    // Ввод трех чисел
    cout << "Введите три положительных числа:" << endl;
    cout << "a = ";
    cin >> a;
    cout << "b = ";
    cin >> b;
    cout << "c = ";
    cin >> c;
    
    // Проверка правильности ввода
    if (cin.fail()) {
        cout << "Ошибка! Нужно вводить только числа!" << endl;
        return 1;
    }
    
    // Проверка на положительность чисел
    if (a <= 0 || b <= 0 || c <= 0) {
        cout << "Ошибка! Все числа должны быть положительными!" << endl;
        return 1;
    }
    
    // Выводим введенные данные
    cout << "Введенные данные" << endl;
    cout << "a = " << a << endl;
    cout << "b = " << b << endl;
    cout << "c = " << c << endl;
    
    // Проверка возможности построения треугольника
    // Условие: сумма любых двух сторон больше третьей
    cout << "Проверка треугольника" << endl;
    
    if ((a + b > c) && (a + c > b) && (b + c > a)) {
        cout << "Из этих отрезков можно построить треугольник." << endl;
        
        // Проверка на остроугольность
        // Треугольник остроугольный, если квадрат каждой стороны
        // меньше суммы квадратов двух других сторон
        float a2 = a * a;
        float b2 = b * b;
        float c2 = c * c;
        
        cout << "Проверка типа треугольника" << endl;
        
        if ((a2 < b2 + c2) && (b2 < a2 + c2) && (c2 < a2 + b2)) {
            cout << "Треугольник является остроугольным." << endl;
        }
        else {
            cout << "Треугольник не является остроугольным." << endl;
            cout << "Это прямоугольный или тупоугольный треугольник." << endl;
        }
    }
    else {
        cout << "Из этих отрезков нельзя построить треугольник." << endl;
        cout << "Не выполняется условие: сумма любых двух сторон > третьей" << endl;
    }
    
    return 0;
}